# Print a list while using reverse function and sort function
dream_places =["Japan", "South-Korea", "Canada", "Philippines","Iceland", "Thailand" ]
print(dream_places)
print(sorted(dream_places))
print(dream_places)
print(sorted(dream_places,reverse=True))
print(dream_places)
dream_places.reverse()
print(dream_places)
dream_places.reverse()
dream_places.sort()
print(dream_places)
dream_places.sort(reverse=True)
print(dream_places)
dream_places.sort()
print(dream_places)
# Printing this will reverse and sort out the list